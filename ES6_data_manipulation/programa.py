# Restricciones:

# La suma de las longitudes de todas las palabras no excede
# Todas las palabras están compuestas únicamente de letras minúsculas del idioma inglés.

# Formato de entrada

# La primera línea contiene el número entero,.
# El siguienteCada línea contiene una palabra.

# Formato de salida

# ProducciónLíneas.
# En la primera línea, se muestra el número de palabras distintas de la entrada.
# En la segunda línea, se muestra el número de ocurrencias de cada palabra distinta según su aparición en la entrada.

# Entrada de muestra

# 4
# bcdef
# abcdefg
# bcde
# bcdef
# Salida de muestra

# 3
# 2 1 1
